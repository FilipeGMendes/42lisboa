#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
/* this does almost the same as
	ft_putstr_fd
	this one just adds a newline at the end
*/