#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	del(lst->content);
	free(lst);
}
/* this function takes a list element as parameter
	and deletes its content
	as well as free the allocated memory
	using the del function
	passed as parameter too
*/