#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
	*lst = NULL;
}
/*	this function works similarly as the ft_lstdelone
	but instead of removing only one element
	it removes the element passed as parameter
	as well as all the following elements
*/